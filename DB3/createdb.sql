    CREATE INDEX INDACTOR ON MOVIE_ACTORS(ACTORNAME);
    CREATE INDEX INDDIRECTOR ON MOVIE_DIRECTORS(DIRECTORNAME);

    CREATE INDEX INDUSER ON USER_TAGGEDMOVIES(USERID);
